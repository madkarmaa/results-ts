import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { run } from 'mitata';

// importing each file registers its benches into mitata's shared collection;
// order of imports = order of groups in the output
import './constructors';
import './result';
import './option';
import './result.async';
import './option.async';

// ---------------------------------------------------------------------------
// Runner modes
// ---------------------------------------------------------------------------
const PUBLISH = process.env.BENCH_PUBLISH === '1';

const main = async () => {
    if (!PUBLISH) {
        await run();
        return;
    }

    const lines: string[] = [];

    lines.push('# Benchmarks');
    lines.push('');
    lines.push('> Performance benchmarks for `Result<T, E>` and `Option<T>`.');
    lines.push(
        '> Allocation-heavy transforms (map, andThen, flatten, transpose, unzip, ...) are emphasized.'
    );
    lines.push(
        '> Results are generated by the `Benchmarks` workflow via [mitata](https://github.com/evanwashere/mitata), and are inherently machine-specific.'
    );
    lines.push('');

    await run({
        format: 'markdown',
        colors: false,
        print: (s: string) => lines.push(s)
    });

    const out = lines.join('\n').trimEnd() + '\n';

    // index.ts lives in <root>/bench, so the repo root is one level up
    const benchDir = resolve(dirname(fileURLToPath(import.meta.url)));
    writeFileSync(resolve(benchDir, '..', 'BENCHMARKS.md'), out);

    console.log('wrote BENCHMARKS.md');
};

main();
